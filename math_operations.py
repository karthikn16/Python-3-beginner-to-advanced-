import math

n = float(input("Enter a number: "))

square = n ** 2
cube = n ** 3
square_root = n ** 0.5
log2_value = math.log2(n)
cos_value = math.cos(n)
exp_value = math.exp(n)   # e^n

print("Square of", n, "=", square)
print("Cube of", n, "=", cube)
print("Square root of", n, "=", square_root)
print("log2(", n, ") =", log2_value)
print("cos(", n, ") =", cos_value)
print("e^", n, "=", exp_value)
